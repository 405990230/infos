package com.bmw.boss.infos.app.pojo.json;

public class ResponseImagesItemPojo {

	private String link;
	private int x;
	private int y;
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
